public class Multiplicationtable {
    public static void main(String[] args) {
        int i = 1;//start number
        do {
            System.out.print(" " + i * 1 );// display
            System.out.print(" " + i * 2 );
            System.out.println(" " + i * 3 );
            i++;
        } while (i <= 3);//up tp 3
    }
}
